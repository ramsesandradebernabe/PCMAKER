    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        String idemp = ptxtIDempleado.getText(); 
        try {
            Statement consul = con.createStatement();
            ResultSet resul = consul.executeQuery("select * from empleados where id_empleado='" + idemp +"'");
            if (resul.next()){
                MenuEmpleado O = new MenuEmpleado(idemp);
                O.setVisible(true);
                dispose();
            } else{
                JOptionPane.showMessageDialog(null,"EL ID NO CORRESPONDE CON NINGUN EMPLEADO DE ESTA SUCURSAL","Error. . .!",JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

