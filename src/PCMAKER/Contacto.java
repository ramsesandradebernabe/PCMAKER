    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        String aux;
        if (txtNombre.getText().equals("")||txtCorreo.getText().equals("")||txtComentario.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"No has llenado todos los campos","Error detectado! ! !",JOptionPane.ERROR_MESSAGE);
        }else{     
            try {   
                PreparedStatement in = con.prepareStatement("INSERT INTO mensajes (id_mensaje,correo_user,mensaje) VALUES (?,?,?)");
                in.setString(1,null);
                in.setString(2,txtCorreo.getText());
                in.setString(3,txtComentario.getText());
                in.executeUpdate();
                JOptionPane.showMessageDialog(null,"Tu mensaje ha sido registrado exitosamente","Proceso Completado. . !",JOptionPane.INFORMATION_MESSAGE);
                txtNombre.setText("");
                txtComentario.setText("");
                txtCorreo.setText("");
            } catch(Exception e){
                JOptionPane.showMessageDialog(null,"El correo ingresado no esta registrado","Error detectado! ! !",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

